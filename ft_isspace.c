#include "libft.h"

int			ft_isspace(int c)
{
	if ((char)c == ' ' || (char)c == '\n' || (char)c == '\t' ||
		(char)c == '\r' || (char)c == '\v' || (char)c == '\f')
	{
		return (1);
	}
	else
		return (0);
}
